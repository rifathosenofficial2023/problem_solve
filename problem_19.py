#সমস্যা:তিনটি বাহু দেওয়া হলে, তা দিয়ে ত্রিভুজ তৈরি করা সম্ভব কিনা তা নির্ণয় করুন।

a = float(input("প্রথম বাহু: "))
b = float(input("দ্বিতীয়বাহু: "))
c = float(input("তৃতীয় বাহু: "))
if a+b>c and a + c> b and b+c>a:
 print("এই বাহুগুলো দিয়ে ত্রিভুজ তৈরি করা সম্ভব")
else:
 print("এই বাহুগুলো দিয়ে ত্রিভুজ তৈরি করা সম্ভব নয়")