
# অবমাননাকারী কাফেরদের বিধান 
one = "ইসলাম কে অবমাননা করলে"
two = "কুরআন কে  অবমাননা করলে"
three = "রাসুল কে অবমাননা করলে"

# ইউজারের ইনপুট নেয়া
number = input("আপনার সংখ্যা লিখুন: ")

# শর্ত অনুযায়ী আউটপুট
if number == one:
    print("আপনার মেয়াদ শেষ")
elif number == two:
    print(" আপনার দুনিয়াতে থাকার অধিকার নেই ")
elif number == three:
    print(" আপনার মৃত্যুর সুসংবাদ গ্রহণ করুন ")
else:
    print("আপনি ভূল জায়গায় হাত দিয়ে ফেলছেন")