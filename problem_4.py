# বিষয়ভিত্তিক বার্তা
one = "ইসলাম সম্পর্কে জানতে চাই"
two = "কুরআন সম্পর্কে জানতে চাই"
three = "রাসুল সম্পর্কে জানতে চাই"

# ইউজারের ইনপুট নেয়া
topic = input("আপনি কোন বিষয়ে জানতে চান? ").strip()

# শর্ত অনুযায়ী আউটপুট
if topic == one:
    print("ইসলাম হচ্ছে শান্তি ও ন্যায়ের ধর্ম।")
elif topic == two:
    print("কুরআন হচ্ছে মানবতার দিকনির্দেশনা।")
elif topic == three:
    print("রাসুল (সা.) ছিলেন দয়ার প্রতীক ও মানবতার শিক্ষক।")
else:
    print("দুঃখিত, আমি বিষয়টি বুঝতে পারিনি।")
